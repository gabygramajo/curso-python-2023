'''
CON NÚMEROS ENTEROS
'''
# suma 
print(f"suma 2 + 5 = {2 + 5}")
print(f"suma 2 + 4.5 = {2 + 4.5}")
print(f"suma 7.5 + 2.5 = {7.5 + 2.5}")
# resta 
print(f"resta 2 - 5 = {2 - 5}")
print(f"resta 2 - 4.5 = {2 - 4.5}")
print(f"resta 7.5 - 2.5 = {7.5 - 2.5}")
# multiplicacion
print(f"multiplición 2 * 5 = {2 * 5}")
print(f"multiplición 2 * 4.5 = {2 * 4.5}")
print(f"multiplición 7.5 * 2.5 = {7.5 * 2.5}")
# división
print(f"dividisión 7 / 2 = {7 / 2}")
print(f"dividisión 8.2 / 2 = {8.2 / 2}")
print(f"dividisión 6 / 2 = {6 / 2}")
# división entera
print(f"dividisión entera 6 // 2 = {6 // 2}")
print(f"dividisión entera 8.2 // 2 = {8.2 // 2}")
# módulo/resto
print(f"módulo 6 % 2 = {6 % 2}")
print(f"módulo 7 % 2 = {7 % 2}")
# Potencia
print(f"Potencia 2**3 = {2**3}")
print(f"Potencia (1.3)**3 = {(1.3)**3}")

